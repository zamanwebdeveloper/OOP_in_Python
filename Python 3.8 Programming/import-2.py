# import datetime
import datetime
time = datetime.time(10,45,20) # hour, minute, second
print('Hour: ',time.hour)
print('Minute: ',time.minute)
print('Second: ',time.second)

date = datetime.date(2020,3,15)# year, month, day
print('\nYear: ',date.year)
print('Month: ',date.month)
print('Day: ',date.day)